package com.kodilla.basic_assertion;

import static java.lang.Math.sqrt;

public class Calculator {

    public double add(double a, double b) {
        return a + b;
    }

    public double sub(double a, double b) {
        return a - b;
    }

    public double sqr(double a) {
        return Math.pow(a,2);
    }
}