package com.kodilla.basic_assertion;

import static java.lang.Math.sqrt;

public class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int sqr(double a) {
        return (int) Math.pow(a, 2);
    }
}