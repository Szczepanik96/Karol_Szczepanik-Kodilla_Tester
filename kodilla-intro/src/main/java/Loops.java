public class Loops {
    public static void main(String[] args) {
        String[] names = new String[] {"Bolek", "Lolek", "Romek", "Atomek", "Hiacynt", "Barabasz", "Adolf", "Hieronim", "Franek", "Jerzyk"};
        for (int i = names.length - 1; i >= 0; i--) {
            System.out.println(names[i]);
        }

    }
}
